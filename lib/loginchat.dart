  Widget _showErrorMessage(){
    if (_errorMessage.length >0 && _errorMessage != null) {
      return new Text(
        _errorMessage,
        style: TextStyle(
                fontSize: 13.0,
                color: Colors.red,
                height: 1.0,
                fontWeight: FontWeight.w300),
      );
    }else{
      return new Container(
        height: 0.0,
      );
    }
  }

  void _onSignInButtonPress() {
    _pageController.animateToPage(0,
        duration: Duration(milliseconds: 500), curve: Curves.decelerate);
  }

  void _onSignUpButtonPress()  {
    _pageController?.animateToPage(1,
        duration: Duration(milliseconds: 500), curve: Curves.decelerate);
  }

  // Check if form is valid before perform login or signup
  bool _validateAndSave() {
    final form = _scaffoldKey.currentState;
    if (form.validate()) {
      form.save();
      return true;
    }
    return false;
  }

  void _showVerifyEmailSentDialog() {
    showDialog(
      context: context,
      builder: (BuildContext context) {
        // return object of type Dialog
        return AlertDialog(
          title: new Text("おめでとう"),
          content: new Text("登録できました"),
          actions: <Widget>[
            new FlatButton(
              child: new Text("完了"),
              onPressed: () {
                signupNameController.text = '';
                signupEmailController.text ='';
                signupPasswordController.text = '';
                Navigator.of(context).pop();
              },
            ),
          ],
        );
      },
    );
  }

  void _signinButtonconfirm() async{
    String userId = "";
    userId = await widget.auth.signIn(loginEmailController.text, loginPasswordController.text);
    if(userId!=""){
      Navigator.push(
        context,
        MaterialPageRoute(builder: (context) => ChatList()),
      );
    }
  }


  void _signupButtonconfirm() async{
      if(_validateAndSave()){
          var userId;
          userId = await widget.auth.signUp(signupEmailController.text, signupPasswordController.text,signupNameController.text);
          _showVerifyEmailSentDialog();
          widget.auth.sendEmailVerification();
      }
  }

  void _toggleLogin() {
    setState(() {
      _obscureTextLogin = !_obscureTextLogin;
    });
  }

  void _toggleSignup() {
    setState(() {
      _obscureTextSignup = !_obscureTextSignup;
    });
  }

  void _toggleSignupConfirm() {
    setState(() {
      _obscureTextSignupConfirm = !_obscureTextSignupConfirm;
